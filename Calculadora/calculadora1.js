function somar (num1, num2){
    return num1+num2
}
function subtrair (num1, num2){
    return num1-num2
}
function multiplicar (num1, num2){
    return num1*num2
}
function dividir (num1, num2){
    return num1/num2
}
console.log(somar(18,27))
console.log(subtrair(18,27))
console.log(multiplicar(18,27))
console.log(dividir(18,27))